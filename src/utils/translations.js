/**
 * Gets translated text by key path
 * @param {string} language - Language code (en, vi)
 * @param {string} path - Dot notation path to the translation (e.g. 'nav.dashboard')
 * @returns {string} - Translated text or key path if translation not found
 */
export const getTranslation = (language, path) => {
  try {
    // Split the path and navigate through the translations object
    const keys = path.split('.');
    let result = translations[language] || translations.en;
    
    for (const key of keys) {
      if (result[key] === undefined) {
        // Return path if translation not found
        return path;
      }
      result = result[key];
    }
    
    return result;
  } catch (error) {
    console.error('Translation error:', error);
    return path; // Return the path as fallback
  }
};

/**
 * Helper function to create a translation function for a specific language
 * @param {string} language - Language code
 * @returns {function} - Function that takes a path and returns translated text
 */
export const createTranslator = (language) => {
  return (path) => getTranslation(language || 'en', path);
};

export default translations;
